public class EndsWithString {
    public static void main(String[] args) {
        String string1 = "Ditia Mutiara Putri";
        System.out.println("Hasilnya adalah:" + string1.endsWith("D"));
        System.out.println("Hasilnya adalah:" + string1.endsWith("p"));
        System.out.println("Hasilnya adalah:" + string1.endsWith("Hello"));
        System.out.println("Hasilnya adalah:" + string1.endsWith("Ditia"));
    }

}